#include "philo.h"

int	ft_isspace(int c)
{
	char	cc;

	cc = (char)c;
	if (cc == '\t' || cc == '\n' || cc == '\v' || cc == '\f'
		|| cc == '\r' || cc == ' ')
		return (1);
	return (0);
}
